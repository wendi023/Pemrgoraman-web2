<?php

$x = 10;

echo "Aku adalah angka $x</br>";

//Perkalian
$hasilKali = $x * 8; //Tanda * adalah operator
echo "jika aku di kali 8, jumlahku sekarang $hasilKali </br>";

//Pembagian
$hasilBagi = $hasilKali / 4; //Tanda / adalah operator
echo "jika aku di bagi 4, jumlahku sekarang $hasilBagi </br>";

//Pengurangan
$hasilKurang = $hasilBagi - 6; //Tanda - adalah operator
echo "jika aku di kurang 6, jumlahku sekarang $hasilKurang </br>";

//Pertambahan
$hasilTambah = $hasilKurang + 2; //Tanda + adalah operator
echo "jika aku di tambah 2, jumlahku sekarang $hasilTambah </br>";
?>